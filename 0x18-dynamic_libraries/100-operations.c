#include <stdio.h>

int add(int a, int b)
{
    int sum = 0;
    sum = a + b;
    return (sum);
}

int sub(int a, int b)
{
    int difference = 0;
    difference = a - b;
    return (difference);
}

int mul(int a, int b)
{
    int product = 0;
    product = a * b;
    return (product);
}

int div(int a, int b)
{
    int division = 0;
    division = a / b;
    return (division);
}

int mod(int a, int b)
{
    int modulus = 0;
    modulus = a % b;
    return (modulus);
}